
public class Medicament {
	private String numeMedicamet;
	private int stoc;

	public Medicament() {
	}

	public void setMedicament(String medicament) {
		this.numeMedicamet = medicament;
	}

	public String getMedicament() {
		return this.numeMedicamet;
	}

	public void setStoc(int stoc) {
		this.stoc = stoc;
	}

	public int getStoc() {
		return this.stoc;
	}

}
